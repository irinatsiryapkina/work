ALTER DEFAULT PRIVILEGES FOR ROLE "wte-admin" IN SCHEMA "ROTUS CAFO" GRANT  USAGE                                     ON TYPES     TO   "wte-ro";
ALTER DEFAULT PRIVILEGES FOR ROLE "wte-admin" IN SCHEMA "ROTUS CAFO" GRANT  INSERT, UPDATE, DELETE                    ON TABLES    TO   "wte-rw";
ALTER DEFAULT PRIVILEGES FOR ROLE "wte-admin" IN SCHEMA "ROTUS CAFO" GRANT  USAGE                                     ON SEQUENCES TO   "wte-rw";
ALTER DEFAULT PRIVILEGES FOR ROLE "wte-admin" IN SCHEMA "ROTUS CAFO" GRANT  EXECUTE                                   ON FUNCTIONS TO   "wte-rw";
